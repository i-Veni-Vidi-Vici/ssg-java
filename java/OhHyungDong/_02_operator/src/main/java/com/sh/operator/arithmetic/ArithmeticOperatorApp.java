package com.sh.operator.arithmetic;

/**
 * <pre>
 *  산술 연산
 *  +
 *  -
 *  *
 *  /
 *  %
 *
 * </pre>
 */
public class ArithmeticOperatorApp {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
