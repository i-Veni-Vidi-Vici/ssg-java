package com.sh.literal;

public class LiteralApp {

    /**
     * (asterisk)
     *
     *  Literal(값)
     *
     * @param args
     */
    public static void main(String[] args) {
        // 1. 정수
        System.out.println(123);
        System.out.println(-123);

        // 2. 실수
        System.out.println(12.345);

        // 논리값 (참 / 거짓)
        System.out.println(true);
        System.out.println(false);

        // 문자(1개) - 홀따옴표로 감싸서 표현
        System.out.println('a');
    }
}
