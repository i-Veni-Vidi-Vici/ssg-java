package com.sh.operator.arithmetic;

public class ArithmeticOperatorApp {
    /**
     * <pre>
     * 산술연산
     * +
     * -
     * *
     * /
     * %
     * </pre>
     */
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
