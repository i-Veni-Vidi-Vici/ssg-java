package com.ssg.test.multi.dimentional.array.MultiDimentionalArray;

public class Print {
    public static void print(int[][] arr){
        for(int i = 0; i < arr.length; i++) {
            for(int j = 0; j < arr[i].length; j++) {
                System.out.print(arr[i][j] + " ");
            }
            System.out.println();
        }
    }
}
